# Operações Matemáticas (Aritiméticas)

"""
    - Soma: +
    - Subitração: - 
    - Multiplicação: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da divisão: %
    - Potência: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(numero1 % 3)
print(numero1 ** 5)


# operadores de Comparação
print(numero2 > numero1) # num2 é maior que
print(numero2 < numero1) # num2 é menor que
print(numero2 >= numero1) # num2 é maior ou igual que
print(numero2 <= numero1) # num2 é menor ou igual
print(numero2 != numero1) # num2 é diferente
print(numero2 == numero1) # num2 é igual