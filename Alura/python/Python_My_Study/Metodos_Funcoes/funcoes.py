

# Saber quantos elementos minha lista possui com a Função Lenght ==> len()

lista =[12,14,25,55,90]
print("Tamanho da minha lista usando o lenght: ", len(lista)) # 5
print()

# somar todos os elementos da minha lista com sum:
print("Somar elementos da minha lesta com a função sum: ", sum(lista)); #196
print()

#captar o maior valor da minha lista com a Função max()
print("Qual o maior elemento da minha lista: ", max(lista)) # 90
print()

#capta o menor valor da minha lista com a Função min()
print("Qual o menor elemento da minha lista: ", min(lista))# 12
print()
