# Operações Matemáticas (Aritiméticas)
"""
- Soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisão Intera: //
- Divisão dda Divisão: %
- Potência: **
"""

numberOne = 10
numbertwo = 20
print(numberOne + numbertwo);
print(numberOne - numbertwo);
print(numberOne * numbertwo);
print(numberOne / numbertwo); # 0.5
print(numberOne // numbertwo);# 0
print(20% 3); # 2
print(2 ** 3);# 8


